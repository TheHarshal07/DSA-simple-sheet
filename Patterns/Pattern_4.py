'''
Print the pattern like:
1
22
333
4444

'''

def NumberPattern(n):
    for i in range(1,n+1):
        for j in range(i):
            print(i,end="")
        print("\r")

def ReverseNumberPattern(n):
    for i in range(n,-1,-1):
        for j in range(i):
            print(i,end="")
        print("\r")

number = int(input("Enter the number :"))
NumberPattern(number)
print("\n")
ReverseNumberPattern(number)



